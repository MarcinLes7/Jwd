public class Laboratorium3 {
    /* Napisz program obliczający pole powierzchni, obwód oraz długość przekątnej dla kwadratów o następujących
     długościach boków 2, 7, 11 oraz 19 */
    public static void main(String[] args) {
        int a = 2;
        System.out.println("Pole powierzchni kwadratu o boku " + a + " = " + (a * a));
        System.out.println("Obwód kwadratu o boku " + a + " = " + (4 * a));
        System.out.println("Długość przekątnej dla kwadratu o boku " + a + " = " + (a / Math.sqrt(2)));
        System.out.println();

        int b = 7;
        System.out.println("Pole powierzchni kwadratu o boku " + b + " = " + (b * b));
        System.out.println("Obwód kwadratu o boku " + b + " = " + (4 * b));
        System.out.println("Długość przekątnej dla kwadratu o boku " + b + " = " + (b / Math.sqrt(2)));
        System.out.println();

        int c = 11;
        System.out.println("Pole powierzchni kwadratu o boku " + c + " = " + (c * c));
        System.out.println("Obwód kwadratu o boku " + c + " = " + (4 * c));
        System.out.println("Długość przekątnej dla kwadratu o boku " + c + " = " + (c / Math.sqrt(2)));
        System.out.println();

        int d = 19;
        System.out.println("Pole powierzchni kwadratu o boku " + d + " = " + (d * d));
        System.out.println("Obwód kwadratu o boku " + d + " = " + (4 * d));
        System.out.println("Długość przekątnej dla kwadratu o boku " + d + " = " + (d / Math.sqrt(2)));
        System.out.println();



    }

}
